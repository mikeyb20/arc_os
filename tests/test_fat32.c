static int test_name_8_3_conversion(void) {
    /* Test fat32_name_to_str */
    Fat32DirEntry e;
    char out[13];

    memcpy(e.name, "HELLO   TXT", 11);
    fat32_name_to_str(&e, out);
    ASSERT_STR_EQ(out, "hello.txt");

    memcpy(e.name, "MYDIR      ", 11);
    fat32_name_to_str(&e, out);
    ASSERT_STR_EQ(out, "mydir");

    memcpy(e.name, "README  MD ", 11);
    fat32_name_to_str(&e, out);
    ASSERT_STR_EQ(out, "readme.md");

    /* Test fat32_str_to_name */
    uint8_t fname[11];
    ASSERT_EQ(fat32_str_to_name("hello.txt", fname), 0);
    ASSERT_MEM_EQ(fname, "HELLO   TXT", 11);

    ASSERT_EQ(fat32_str_to_name("mydir", fname), 0);
    ASSERT_MEM_EQ(fname, "MYDIR      ", 11);

    /* Name too long should fail */
    ASSERT_EQ(fat32_str_to_name("toolongname.txt", fname), -1);
    ASSERT_EQ(fat32_str_to_name("file.toolong", fname), -1);

    return 0;
}

