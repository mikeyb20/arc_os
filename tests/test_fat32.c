static int test_unlink_fat32_file(void) {
    VfsNode *root = mount_writable_copy();
    ASSERT_TRUE(root != NULL);

    /* hello.txt exists from the test image */
    VfsNode *node = root->ops->lookup(root, "hello.txt");
    ASSERT_TRUE(node != NULL);

    int ret = root->ops->unlink(root, "hello.txt");
    ASSERT_EQ(ret, 0);

    /* Should no longer appear in lookup */
    node = root->ops->lookup(root, "hello.txt");
    ASSERT_TRUE(node == NULL);
    unmount_test_disk();
    return 0;
}

static int test_truncate_to_zero(void) {
    VfsNode *root = mount_writable_copy();
    ASSERT_TRUE(root != NULL);

    VfsNode *node = root->ops->lookup(root, "hello.txt");
    ASSERT_TRUE(node != NULL);
    ASSERT_TRUE(node->size > 0);

    node->ops->truncate(node, 0);
    ASSERT_EQ(node->size, 0);

    char buf[16];
    int n = node->ops->read(node, buf, 0, sizeof(buf));
    ASSERT_EQ(n, 0);
    unmount_test_disk();
    return 0;
}

