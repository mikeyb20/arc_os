/* Allocation flags */
#define GFP_KERNEL  0x00
#define GFP_ZERO    0x01

/* kmalloc/kfree stubs using libc */
static void *kmalloc(size_t size, uint32_t flags) {
    void *p = malloc(size);
    if (p && (flags & GFP_ZERO)) memset(p, 0, size);
    return p;
}
static void kfree(void *ptr) { free(ptr); }
static void *krealloc(void *ptr, size_t new_size) { return realloc(ptr, new_size); }

/* VirtIO-blk stub: reads/writes from a file on disk */
static FILE *disk_file;

static int virtio_blk_read(uint64_t sector, uint32_t count, void *buf) {
    if (!disk_file) return -1;
    if (fseek(disk_file, (long)(sector * 512), SEEK_SET) != 0) return -1;
    return (fread(buf, 512, count, disk_file) == count) ? 0 : -1;
}

static int virtio_blk_write(uint64_t sector, uint32_t count, const void *buf) {
    if (!disk_file) return -1;
    if (fseek(disk_file, (long)(sector * 512), SEEK_SET) != 0) return -1;
    if (fwrite(buf, 512, count, disk_file) != count) return -1;
    fflush(disk_file);
    return 0;
}

/* Include FAT32 implementation only (vfs.c already compiled in test_vfs.c) */
#include "../kernel/fs/fat32.c"

/* Path to the test disk image — generated by tools/make-fat32-test-disk.sh */
static const char *test_disk_path = "tests/fat32_test.img";

/* Helper: open disk, mount FAT32, return root node */
static VfsNode *mount_test_disk(void) {
    disk_file = fopen(test_disk_path, "r+b");
    if (!disk_file) {
        /* Try from build directory */
        disk_file = fopen("../tests/fat32_test.img", "r+b");
    }
    if (!disk_file) return NULL;

    /* Reset FAT32-internal state */
    node_cache_count = 0;
    next_fat_inode = 0x10000;

    return fat32_mount();
}

static void unmount_test_disk(void) {
    if (disk_file) {
        fclose(disk_file);
        disk_file = NULL;
    }
}

/* Helper to create a fresh writable copy of the test disk for write tests */
static VfsNode *mount_writable_copy(void) {
    /* Copy original to temp file */
    FILE *src = fopen(test_disk_path, "rb");
    if (!src) src = fopen("../tests/fat32_test.img", "rb");
    if (!src) return NULL;

    FILE *dst = tmpfile();
    if (!dst) { fclose(src); return NULL; }

    char buf[4096];
    size_t n;
    while ((n = fread(buf, 1, sizeof(buf), src)) > 0) {
        fwrite(buf, 1, n, dst);
    }
    fclose(src);
    fflush(dst);
    rewind(dst);

    disk_file = dst;
    node_cache_count = 0;
    next_fat_inode = 0x10000;

    return fat32_mount();
}

/* === Read-path tests === */

static int test_bpb_parse_valid(void) {
    VfsNode *root = mount_test_disk();
    ASSERT_TRUE(root != NULL);
    ASSERT_EQ(root->type, VFS_DIRECTORY);
    unmount_test_disk();
    return 0;
}

static int test_bpb_reject_bad_sector_size(void) {
    /* Create a tiny invalid image */
    disk_file = tmpfile();
    ASSERT_TRUE(disk_file != NULL);
    /* Write 512 bytes of zeros — bytes_per_sector will be 0 */
    char zeros[512];
    memset(zeros, 0, sizeof(zeros));
    fwrite(zeros, 1, sizeof(zeros), disk_file);
    fflush(disk_file);
    rewind(disk_file);

    node_cache_count = 0;
    VfsNode *root = fat32_mount();
    ASSERT_TRUE(root == NULL);
    fclose(disk_file);
    disk_file = NULL;
    return 0;
}

static int test_fat_table_loaded(void) {
    VfsNode *root = mount_test_disk();
    ASSERT_TRUE(root != NULL);
    Fat32NodeInfo *info = to_fat32(root);
    /* FAT entries for cluster 2 (root) should be non-free */
    uint32_t root_entry = info->vol->fat[info->vol->root_cluster] & FAT32_ENTRY_MASK;
    ASSERT_TRUE(root_entry != FAT32_FREE);
    unmount_test_disk();
    return 0;
}

static int test_root_readdir(void) {
    VfsNode *root = mount_test_disk();
    ASSERT_TRUE(root != NULL);

    VfsDirEntry entries[16];
    int count = root->ops->readdir(root, entries, 16);
    ASSERT_TRUE(count >= 2);  /* hello.txt and subdir at minimum */

    /* Find hello.txt and subdir */
    int found_hello = 0, found_subdir = 0;
    for (int i = 0; i < count; i++) {
        if (strcmp(entries[i].name, "hello.txt") == 0) {
            found_hello = 1;
            ASSERT_EQ(entries[i].type, VFS_FILE);
        }
        if (strcmp(entries[i].name, "subdir") == 0) {
            found_subdir = 1;
            ASSERT_EQ(entries[i].type, VFS_DIRECTORY);
        }
    }
    ASSERT_TRUE(found_hello);
    ASSERT_TRUE(found_subdir);
    unmount_test_disk();
    return 0;
}

