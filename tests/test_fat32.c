static int test_create_file(void) {
    VfsNode *root = mount_writable_copy();
    ASSERT_TRUE(root != NULL);

    VfsNode *node = root->ops->create(root, "new.txt", VFS_FILE);
    ASSERT_TRUE(node != NULL);
    ASSERT_EQ(node->type, VFS_FILE);

    /* Verify it appears in readdir */
    VfsDirEntry entries[16];
    int count = root->ops->readdir(root, entries, 16);
    int found = 0;
    for (int i = 0; i < count; i++) {
        if (strcmp(entries[i].name, "new.txt") == 0) found = 1;
    }
    ASSERT_TRUE(found);
    unmount_test_disk();
    return 0;
}

static int test_write_then_read_fat32(void) {
    VfsNode *root = mount_writable_copy();
    ASSERT_TRUE(root != NULL);

    VfsNode *node = root->ops->create(root, "test.txt", VFS_FILE);
    ASSERT_TRUE(node != NULL);

    const char *data = "FAT32 write test!";
    int written = node->ops->write(node, data, 0, 17);
    ASSERT_EQ(written, 17);

    char buf[32];
    memset(buf, 0, sizeof(buf));
    int n = node->ops->read(node, buf, 0, sizeof(buf));
    ASSERT_EQ(n, 17);
    ASSERT_MEM_EQ(buf, "FAT32 write test!", 17);
    unmount_test_disk();
    return 0;
}

